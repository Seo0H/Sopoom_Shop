package com.sopoom.dto;

public class ProductVO {
	private String p_id;
	private String p_name;
	private int p_unitPrice;
	private String p_description;
	private String p_category;
	private String p_manufacturer;
	private int p_unitsInStock;
	private String p_condition;
	private String p_fileName;

	public String getP_id() {
		return p_id;
	}

	public void setP_id(String p_id) {
		this.p_id = p_id;
	}

	public String getP_name() {
		return p_name;
	}

	public void setP_name(String p_name) {
		this.p_name = p_name;
	}

	public int getP_unitPrice() {
		return p_unitPrice;
	}

	public void setP_unitPrice(int p_unitPrice) {
		this.p_unitPrice = p_unitPrice;
	}

	public String getP_description() {
		return p_description;
	}

	public void setP_description(String p_description) {
		this.p_description = p_description;
	}

	public String getP_category() {
		return p_category;
	}

	public void setP_category(String p_category) {
		this.p_category = p_category;
	}

	public String getP_manufacturer() {
		return p_manufacturer;
	}

	public void setP_manufacturer(String p_manufacturer) {
		this.p_manufacturer = p_manufacturer;
	}

	public int getP_unitsInStock() {
		return p_unitsInStock;
	}

	public void setP_unitsInStock(int p_unitsInStock) {
		this.p_unitsInStock = p_unitsInStock;
	}

	public String getP_condition() {
		return p_condition;
	}

	public void setP_condition(String p_condition) {
		this.p_condition = p_condition;
	}

	public String getP_fileName() {
		return p_fileName;
	}

	public void setP_fileName(String p_fileName) {
		this.p_fileName = p_fileName;
	}

}